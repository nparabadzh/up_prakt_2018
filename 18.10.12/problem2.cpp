#include <iostream>
using namespace std;

int main()
{
    int n, m;
    cout << "Enter the first number: ";
    cin >> n;
    cout << "Enter the second number: ";
    cin >> m;
    cout << "Sum: " << n + m << endl;
    cout << "Difference: " << n - m << endl;
    cout << "Product: " << n * m << endl;
    cout << "Quotient: " << n / m << endl;
    return 0;
}
